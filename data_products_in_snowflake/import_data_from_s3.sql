USE ROLE R_SAMPLE_DAAP_MASTER;
-- CREATE A STAGE IN YOUR RAW SCHEMA
SHOW INTEGRATIONS;
DESCRIBE INTEGRATION "stg_generic-27b70f5";

CREATE OR REPLACE STAGE DB_DATA_PRODUCTS.SAMPLE_DAAP_RAW.SAMPLE_STAGE
STORAGE_INTEGRATION = "stg_generic-27b70f5"
